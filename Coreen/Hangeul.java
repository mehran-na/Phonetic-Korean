package Coreen;

public class Hangeul {
    private int ci;
    private int vi;
    private int di;

    public Hangeul(int ci, int vi, int di) {
        this.ci = ci;
        this.vi = vi;
        this.di = di;
    }

    public int getCi() {
        return ci;
    }

    public int getVi() {
        return vi;
    }

    public int getDi() {
        return di;
    }
}
